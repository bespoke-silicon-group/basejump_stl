import random
import sys

class TraceGen:

  def print_header(self):
    print("#### generated by trace_gen.py ####")

  def tagst(self, addr, data):
    packet = "0001_"
    packet += "10000_"                      # tagst
    packet += format(addr, "032b") + "_"    # addr
    packet += format(data, "032b")             # data
    print(packet)

  def alock(self, addr):
    packet = "0001_"
    packet += "11011_"                      # alock
    packet += format(addr, "032b") + "_"    # addr
    packet += format(0, "032b")             # data
    print(packet)

  def aunlock(self, addr):
    packet = "0001_"
    packet += "11100_"                      # aunlock
    packet += format(addr, "032b") + "_"    # addr
    packet += format(0, "032b")             # data
    print(packet)

  def load_word(self, addr):
    packet = "0001_"
    packet += "00010_"                      # lw
    packet += format(addr, "032b") + "_"    # addr
    packet += format(0, "032b")             # data
    print(packet)

  def store_word(self, addr, data):
    packet = "0001_"
    packet += "01010_"                      # sw
    packet += format(addr, "032b") + "_"    # addr
    packet += format(data, "032b")          # data
    print(packet)

  def wait_one_cycle(self):
    packet = "0000_"
    packet += "00000_"
    packet += format(0, "032b") +  "_"
    packet += format(0, "032b")
    print(packet)

  def wait(self, cycle):
    packet = "0110_"
    packet += "00000_"
    packet += format(0, "032b") +  "_"
    packet += format(cycle, "032b")
    print(packet)
    packet = "0101_"
    packet += "00000_"
    packet += format(0, "032b") +  "_"
    packet += format(0, "032b")
    print(packet)


if __name__ == "__main__":
  num_instr_p = int(sys.argv[1])  
  seed_p = int(sys.argv[2])
  ways_p = int(sys.argv[3])
  sets_p = 4
  print("# seed_p = " + str(seed_p))

  random.seed(seed_p)

  tg = TraceGen()
  tg.print_header()
  for i in range(sets_p*ways_p):
    tg.tagst(addr=(i<<4), data=0)

